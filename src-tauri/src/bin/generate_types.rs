//! Codegen binary — generates src/types/generated.ts from Rust structs.
//! Run via: cargo run --bin generate_types  (from src-tauri/)
//! Or:       npm run generate-types          (from project root)

use remote_manager_lib::commands::credentials::ResolvedCredentials;
use remote_manager_lib::commands::recording::RecordingInfo;
use remote_manager_lib::commands::vault::VaultStatus;
use remote_manager_lib::database::{
    AuditEntry, AuditVerifyEntry, AuditVerifyResult, ConnectionSummary, CreateConnectionRequest,
    CreateCredentialProfileRequest, CreateSavedCommandRequest, CreateSshKeyRequest,
    CredentialProfile, CredentialType, ExportData, Group, SavedCommand, ServerConnection, SshKey,
    SshTunnel, UpdateConnectionRequest, UpdateCredentialProfileRequest, UpdateSavedCommandRequest,
};
use remote_manager_lib::commands::ssh_keys::SshKeyCreateRequest;
use remote_manager_lib::docker::DockerContainer;
use remote_manager_lib::import::ImportedConnection;
use remote_manager_lib::proxmox::ProxmoxPinnedCert;
use remote_manager_lib::totp::TotpCode;
use remote_manager_lib::network::NetworkScanResult;
use remote_manager_lib::proxmox::{ProxmoxAuthResponse, ProxmoxResource};
use remote_manager_lib::rdp::RdpAvailability;
use remote_manager_lib::sftp_ftp::{FileListResult, FileNode};
use remote_manager_lib::ssh::{SshDataEvent, SshStatusEvent};
use remote_manager_lib::tools::ToolResult;
use remote_manager_lib::vnc::VncAvailability;
use ts_rs::{Config, TS};

fn export<T: TS + 'static>(cfg: &Config) -> String {
    let decl = T::decl(cfg);
    if decl.starts_with("export ") {
        format!("{}\n", decl)
    } else {
        format!("export {}\n", decl)
    }
}

fn main() {
    // Use `number` for i64/u64 since the frontend expects JS number, not bigint.
    let cfg = Config::new().with_large_int("number");

    let mut out = String::new();
    out.push_str(
        "// Auto-generated by 90-20 (ts-rs) — run `npm run generate-types` to refresh.\n\
         // Do not edit manually. Changes will be overwritten.\n\n",
    );

    // ── Primitives / enums first (depended on by later types) ──────────────
    out.push_str(&export::<CredentialType>(&cfg));
    out.push('\n');
    out.push_str(&export::<SshTunnel>(&cfg));
    out.push('\n');

    // ── Core domain types ──────────────────────────────────────────────────
    out.push_str(&export::<Group>(&cfg));
    out.push('\n');
    out.push_str(&export::<CredentialProfile>(&cfg));
    out.push('\n');
    out.push_str(&export::<CreateCredentialProfileRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<UpdateCredentialProfileRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<ResolvedCredentials>(&cfg));
    out.push('\n');
    out.push_str(&export::<ConnectionSummary>(&cfg));
    out.push('\n');
    out.push_str(&export::<ServerConnection>(&cfg));
    out.push('\n');
    out.push_str(&export::<CreateConnectionRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<UpdateConnectionRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<ExportData>(&cfg));
    out.push('\n');
    out.push_str(&export::<SavedCommand>(&cfg));
    out.push('\n');
    out.push_str(&export::<CreateSavedCommandRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<UpdateSavedCommandRequest>(&cfg));
    out.push('\n');

    // ── Vault ──────────────────────────────────────────────────────────────
    out.push_str(&export::<VaultStatus>(&cfg));
    out.push('\n');

    // ── Protocol-specific types ────────────────────────────────────────────
    out.push_str(&export::<RdpAvailability>(&cfg));
    out.push('\n');
    out.push_str(&export::<VncAvailability>(&cfg));
    out.push('\n');
    out.push_str(&export::<ProxmoxAuthResponse>(&cfg));
    out.push('\n');
    out.push_str(&export::<ProxmoxResource>(&cfg));
    out.push('\n');
    out.push_str(&export::<DockerContainer>(&cfg));
    out.push('\n');

    // ── Network / tools ────────────────────────────────────────────────────
    out.push_str(&export::<NetworkScanResult>(&cfg));
    out.push('\n');
    out.push_str(&export::<TotpCode>(&cfg));
    out.push('\n');
    out.push_str(&export::<ToolResult>(&cfg));
    out.push('\n');

    // ── SSH event types ────────────────────────────────────────────────────
    out.push_str(&export::<SshStatusEvent>(&cfg));
    out.push('\n');
    out.push_str(&export::<SshDataEvent>(&cfg));
    out.push('\n');

    // ── File manager ──────────────────────────────────────────────────────
    out.push_str(&export::<FileNode>(&cfg));
    out.push('\n');
    out.push_str(&export::<FileListResult>(&cfg));
    out.push('\n');

    // ── SSH key vault & recording ─────────────────────────────────────────
    out.push_str(&export::<SshKey>(&cfg));
    out.push('\n');
    out.push_str(&export::<CreateSshKeyRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<SshKeyCreateRequest>(&cfg));
    out.push('\n');
    out.push_str(&export::<ProxmoxPinnedCert>(&cfg));
    out.push('\n');
    out.push_str(&export::<RecordingInfo>(&cfg));
    out.push('\n');

    // ── Import ────────────────────────────────────────────────────────────
    out.push_str(&export::<ImportedConnection>(&cfg));
    out.push('\n');

    // ── Audit log ─────────────────────────────────────────────────────────
    out.push_str(&export::<AuditEntry>(&cfg));
    out.push('\n');
    out.push_str(&export::<AuditVerifyEntry>(&cfg));
    out.push('\n');
    out.push_str(&export::<AuditVerifyResult>(&cfg));
    out.push('\n');

    // Write output file relative to the manifest directory (src-tauri/)
    let dest = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("src-tauri parent not found")
        .join("src/types/generated.ts");

    std::fs::write(&dest, &out).unwrap_or_else(|e| {
        eprintln!("Failed to write {}: {}", dest.display(), e);
        std::process::exit(1);
    });

    println!("Generated: {}", dest.display());
}
